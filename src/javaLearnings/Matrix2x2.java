package javaLearnings;

public class Matrix2x2 {
	//to store values as 
//	00 01 02 03
//	10 11 12 13
//	20 21 22 23
//	30 31 32 33

	public static void main(String[] args) {
		for(int i=0;i<=3;i++)
		{
			for(int j=0;j<=3;j++)
			{
				System.out.print(i+""+j+" ");
			}
			System.out.println();
		}

	}

}



	